
package com.ss.pojo;

import java.util.Date;

import com.ss.criteria.CommentActivityCriteria;

/**
 * [社团活动评论实体类]<p>
 * [功能详细描述]<p>
 * @author zzb
 * @version 1.0, 2016年1月16日
 * @see
 * @since zhubu-V1001
 */
public class Comment_activity extends CommentActivityCriteria
{
    /**
     * 主键
     */
    private Integer id;

    /**
     * 个人id/外键
     */
    private Integer buid;

    /**
     * 社团活动id/外键
     */
    private Integer moauid;

    /**
     * 评论内容
     */
    private String contentValue;

    /**
     * 评论时间
     */
    private Date time;

    /**
     * 状态 0 正常 1审核不通过 2审核中
     */
    private Byte state;

    public Integer getId()
    {
        return id;
    }

    public void setId(Integer id)
    {
        this.id = id;
    }

    public Integer getBuid()
    {
        return buid;
    }

    public void setBuid(Integer buid)
    {
        this.buid = buid;
    }

    public Integer getMoauid()
    {
        return moauid;
    }

    public void setMoauid(Integer moauid)
    {
        this.moauid = moauid;
    }

    public String getContentValue()
    {
        return contentValue;
    }

    public void setContentValue(String contentValue)
    {
        this.contentValue = contentValue;
    }

    public Date getTime()
    {
        return time;
    }

    public void setTime(Date time)
    {
        this.time = time;
    }

    public Byte getState()
    {
        return state;
    }

    public void setState(Byte state)
    {
        this.state = state;
    }

}
