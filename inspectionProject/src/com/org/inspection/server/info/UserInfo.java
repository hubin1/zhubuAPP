package com.org.inspection.server.info;

public class UserInfo {

	private int id;
	
	private String create_time;
	
	private String change_pwd_time;
	
	private int lock_status;
	
	private String login_name;
	
	private String remark;
	
	private int status;
	
	private String update_time;
	
	private String user_name;
	
	private String user_pwd;
	
	private String terminal_id;
	
	private int dept_id;
	
	private String position_name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCreate_time() {
		return create_time;
	}

	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}

	public String getChange_pwd_time() {
		return change_pwd_time;
	}

	public void setChange_pwd_time(String change_pwd_time) {
		this.change_pwd_time = change_pwd_time;
	}

	public int getLock_status() {
		return lock_status;
	}

	public void setLock_status(int lock_status) {
		this.lock_status = lock_status;
	}

	public String getLogin_name() {
		return login_name;
	}

	public void setLogin_name(String login_name) {
		this.login_name = login_name;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public String getUpdate_time() {
		return update_time;
	}

	public void setUpdate_time(String update_time) {
		this.update_time = update_time;
	}

	public String getUser_name() {
		return user_name;
	}

	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}

	public String getUser_pwd() {
		return user_pwd;
	}

	public void setUser_pwd(String user_pwd) {
		this.user_pwd = user_pwd;
	}

	public String getTerminal_id() {
		return terminal_id;
	}

	public void setTerminal_id(String terminal_id) {
		this.terminal_id = terminal_id;
	}

	public int getDept_id() {
		return dept_id;
	}

	public void setDept_id(int dept_id) {
		this.dept_id = dept_id;
	}

	public String getPosition_name() {
		return position_name;
	}

	public void setPosition_name(String position_name) {
		this.position_name = position_name;
	}
	
	
	
}
